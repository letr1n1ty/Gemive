export function normalizeLocale(locale) {
  if (!locale) return 'zh-Hant';
  const value = String(locale).toLowerCase();
  if (value === 'zh-hans' || value.startsWith('zh-cn') || value.startsWith('zh-sg') || value.startsWith('zh-hans')) return 'zh-Hans';
  if (value === 'en' || value.startsWith('en-')) return 'en';
  if (value.startsWith('zh')) return 'zh-Hant';
  return 'en';
}

export function getBrowserLocale() {
  return normalizeLocale(globalThis.navigator?.language || 'zh-Hant');
}

export function resolveLocale(settings) {
  return normalizeLocale(settings?.ui?.locale || getBrowserLocale());
}

export function t(locale, key, vars = {}) {
  const normalized = normalizeLocale(locale);
  const template = MESSAGES[normalized]?.[key] ?? MESSAGES['zh-Hant'][key] ?? MESSAGES.en[key] ?? key;
  return String(template).replace(/\{(\w+)\}/g, (_, name) => vars[name] ?? '');
}

export function localizeDocument(locale, root = document) {
  root.documentElement?.setAttribute?.('lang', normalizeLocale(locale));
  root.querySelectorAll('[data-i18n]').forEach((node) => {
    node.textContent = t(locale, node.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-title]').forEach((node) => {
    const value = t(locale, node.dataset.i18nTitle);
    node.setAttribute('title', value);
    node.setAttribute('aria-label', value);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    node.setAttribute('placeholder', t(locale, node.dataset.i18nPlaceholder));
  });
}

export function formatUiLocaleLabel(code) {
  return UI_LOCALES.find((locale) => locale.code === code)?.label || code;
}
